let a = 10;

//Mayor que > o Mayor o igual >= o 
//Menor que < o Menor o igual <=
console.log(a>5); 
console.log(a>=5); 
console.log(a<5); 
console.log(a<=5); 


//Igual == o Diferente !=
console.log(a==5);
console.log(a!=5);
//Comparacion estricta
console.log(a== '10', "==");
console.log(a != '10', "!=");

//Comparacion de tipos
console.log(a === "10", "==="); 
//Diferente de tipos
console.log(a !== "10", "!==");